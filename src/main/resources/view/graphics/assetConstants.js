export const INVENTORY_POSITION = {
  0: [],
  1: []
}

for (let playerIdx = 0; playerIdx < 2; ++playerIdx) {
  const rows = [
    {
      from: 'topLeft',
      to: 'topRight'
    },
    {
      from: 'bottomLeft',
      to: 'bottomRight'
    }
  ]

  for (const row of rows) {
    const a = INVENTORY_RECT[playerIdx][row.from]
    const b = INVENTORY_RECT[playerIdx][row.to]
    for (let ingredientIdx = 0; ingredientIdx < 5; ++ingredientIdx) {
      const p = utils.lerpPosition(a, b, ingredientIdx / 4)
      INVENTORY_POSITION[playerIdx].push(p)
    }
  }
}

